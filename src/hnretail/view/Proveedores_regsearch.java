    public boolean todolleno(){
        boolean paso = true;
        if (nombreProv.getText().trim().isEmpty()){
            paso = false;
            nombreProv.requestFocus();
            nombreProv.setBackground(Color.RED);
            return paso;            
        }
        if (direccionProv.getText().trim().isEmpty()){
            paso = false;
            direccionProv.requestFocus();
            direccionProv.setBackground(Color.RED);
            return paso;            
        }
        if (telPro.getText().trim().isEmpty()){
            paso = false;
            telPro.requestFocus();
            telPro.setBackground(Color.RED);
            return paso;            
        }
        if (webPro.getText().trim().isEmpty()){
            paso = false;
            webPro.requestFocus();
            webPro.setBackground(Color.RED);
            return paso;            
        }
        if (emailPro.getText().trim().isEmpty()){
            paso = false;
            emailPro.requestFocus();
            emailPro.setBackground(Color.RED);
            return paso;            
        }
        if (contactoPro.getText().trim().isEmpty()){
            paso = false;
            contactoPro.requestFocus();
            contactoPro.setBackground(Color.RED);
            return paso;            
        }
        return paso;        
    }
    
