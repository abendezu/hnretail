    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (todolleno()){              
            Proveedor prov = new Proveedor();
            prov.setNombre(nombreProv.getText());
            prov.setDireccion(direccionProv.getText());
            prov.setTelefono(telPro.getText());
            prov.setWebpage(webPro.getText());
            prov.setEmail(emailPro.getText());
            prov.setContacto(contactoPro.getText());
            dA = new dataAccess();
            if (dA.registra_proveedor(prov)){
                JOptionPane.showMessageDialog(null, "Listo", "Paso", JOptionPane.OK_OPTION);
                clearScreen();                
                load_table();
                nombreProv.requestFocus();
            }else {
                JOptionPane.showMessageDialog(null, "No se pudo Guardar", "Error", JOptionPane.WARNING_MESSAGE);
            }
        }else {
            System.out.println("Nop no entro");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

