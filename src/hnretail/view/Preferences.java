    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (todolleno()){
        try {
            String path = System.getProperty("user.dir");            
            Properties props = new Properties();
            char[] pass = PasswordField.getPassword();
            String passString = new String(pass);        
            props.setProperty("database.password", ""+passString);            
            props.setProperty("database.user", ""+UsernameField.getText());            
            props.setProperty("database.url", ""+URLField.getText());            
            props.setProperty("database.driver", DriverField.getText());            
            File f = new File(path+"\\src\\utils\\connection.properties");
            OutputStream out = new FileOutputStream( f );
            props.store(out, "Database Connection Parameters, Do not modify");
        } catch (Exception e ) {
                System.out.println(e);
        }
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

