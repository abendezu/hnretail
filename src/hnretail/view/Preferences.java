    public void load_preferences(){
        try{
            
            String path = System.getProperty("user.dir");
            //System.out.println(path+"\\src\\utils");
            File propertiesFile = new File(path+"\\src\\utils\\connection.properties");
        
            FileReader fileReader = new FileReader(propertiesFile);

            Properties props = new Properties();
            props.load(fileReader);
            
            DriverField.setText(props.getProperty("database.driver"));
            URLField.setText(props.getProperty("database.url"));
            UsernameField.setText(props.getProperty("database.user"));
            PasswordField.setText(props.getProperty("database.password"));        
            RetypeField.setText(props.getProperty("database.password"));                    
            char[] pass = PasswordField.getPassword();
            String passString = new String(pass);
            System.out.println("Este es el password "+ passString.toString()  );
            
        } catch(Exception e){
            System.out.println(e);
        }
        
    }
    
    public boolean todolleno(){
        boolean paso = true;
        if (DriverField.getText().trim().isEmpty()){
            paso = false;
            DriverField.requestFocus();
            DriverField.setBackground(Color.RED);
            return paso;            
        }
        if (URLField.getText().trim().isEmpty()){
            paso = false;
            URLField.requestFocus();
            URLField.setBackground(Color.RED);
            return paso;            
        }
        if (UsernameField.getText().trim().isEmpty()){
            paso = false;
            UsernameField.requestFocus();
            UsernameField.setBackground(Color.RED);
            return paso;            
        }       
        char[] pass = PasswordField.getPassword();
        String passString = new String(pass);
        char[] retype = RetypeField.getPassword();
        String retypeString = new String(retype); 
        System.out.println(passString);
        System.out.println(retypeString);
        if (!passString.equals(retypeString)){
            paso = false;
            AlertLabel.setText("Password y Retype deben de coincidir");
            AlertLabel.setBackground(Color.red);
            PasswordField.requestFocus();
            PasswordField.setBackground(Color.yellow);
            RetypeField.setBackground(Color.yellow);
            return paso;                        
        }                
        return paso;        
    }    
